# | Macro        | snake_case | `my_macro`                 | - Macros let you generate code at compile time. => like defining reusable logic or annotations via code.
# | Struct Field | snake_case | `:email`, `:created_at`    | - Similar to class properties or associative array keys.
# | Atom         | snake_case | `:ok`, `:user_not_found`   | - Similar to constants or enum values
